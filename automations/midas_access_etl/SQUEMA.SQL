-- =====================================================
-- VIEW OPERACIONAL
-- =====================================================

create view analytics.vw_acessos_diarios as
select
date(data_hora) as referencia,
count(*) as total_acessos
from curated.acessos_corretores_imoveis
group by 1;

-- =====================================================
-- VIEW FUNCIONALIDADES MAIS ACESSADAS
-- =====================================================

create view analytics.vw_funcionalidades_mais_acessadas as
select
funcao_acessada,
count(*) as total_acessos
from curated.acessos_corretores_imoveis
group by funcao_acessada;
