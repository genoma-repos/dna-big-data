create table if not exists monitoring.pipeline_executions (
	id uuid primary key default gen_random_uuid(),
	pipeline_name varchar(100) not null,
	started_at timestamptz not null,
	finished_at timestamptz,
	status varchar(20) not null,
	records_extracted integer not null default 0,
	records_transformed integer not null default 0,
	records_loaded integer not null default 0,
	duration_seconds numeric(18, 3),
	error_message text,
	metadata jsonb not null default '{}'::jsonb,
	created_at timestamptz not null default now()
);

create index if not exists idx_pipeline_executions_pipeline_name
	on monitoring.pipeline_executions (pipeline_name);

create index if not exists idx_pipeline_executions_status
	on monitoring.pipeline_executions (status);

create index if not exists idx_pipeline_executions_started_at
	on monitoring.pipeline_executions (started_at);

create table if not exists monitoring.pipeline_logs (
	id uuid primary key default gen_random_uuid(),
	execution_id uuid references monitoring.pipeline_executions (id) on delete set null,
	pipeline_name varchar(100) not null,
	step varchar(100) not null,
	status varchar(20) not null,
	message text not null,
	duration_ms integer,
	metadata jsonb not null default '{}'::jsonb,
	created_at timestamptz not null default now()
);

create index if not exists idx_pipeline_logs_execution_id
	on monitoring.pipeline_logs (execution_id);

create index if not exists idx_pipeline_logs_step
	on monitoring.pipeline_logs (step);

create index if not exists idx_pipeline_logs_status
	on monitoring.pipeline_logs (status);

create table if not exists monitoring.pipeline_alerts (
	id uuid primary key default gen_random_uuid(),
	execution_id uuid references monitoring.pipeline_executions (id) on delete set null,
	pipeline_name varchar(100) not null,
	alert_type varchar(100) not null,
	severity varchar(20) not null,
	title varchar(255) not null,
	message text,
	sent boolean not null default false,
	sent_at timestamptz,
	metadata jsonb not null default '{}'::jsonb,
	created_at timestamptz not null default now()
);

create index if not exists idx_pipeline_alerts_execution_id
	on monitoring.pipeline_alerts (execution_id);

create index if not exists idx_pipeline_alerts_alert_type
	on monitoring.pipeline_alerts (alert_type);

create index if not exists idx_pipeline_alerts_severity
	on monitoring.pipeline_alerts (severity);

