create extension if not exists pgcrypto;

create schema if not exists raw;
create schema if not exists curated;
create schema if not exists monitoring;
create schema if not exists analytics;

create table if not exists raw.midas_access_payloads (
	id uuid primary key default gen_random_uuid(),
	extraction_date timestamptz not null,
	source varchar(100) not null,
	payload jsonb not null,
	created_at timestamptz not null default now()
);

create index if not exists idx_raw_midas_access_payloads_extraction_date
	on raw.midas_access_payloads (extraction_date);

create table if not exists curated.acessos_corretores_imoveis (
	id uuid primary key default gen_random_uuid(),
	data_hora timestamptz not null,
	funcao_acessada varchar(255) not null,
	proprietario varchar(255),
	local varchar(255),
	acessou_pelo varchar(255),
	acessou_telefone boolean,
	usuario varchar(255) not null,
	filial varchar(255),
	codigo_imovel varchar(50) not null,
	endereco text,
	tipo_imovel varchar(100),
	bairro varchar(150),
	dormitorios integer,
	acesso varchar(255),
	origem_acesso varchar(100) not null,
	data_carga timestamptz not null default now(),
	created_at timestamptz not null default now(),
	updated_at timestamptz not null default now(),
	constraint ux_acessos_corretor unique (data_hora, usuario, codigo_imovel)
);

create index if not exists idx_acessos_data_hora
	on curated.acessos_corretores_imoveis (data_hora);

create index if not exists idx_acessos_usuario
	on curated.acessos_corretores_imoveis (usuario);

create index if not exists idx_acessos_filial
	on curated.acessos_corretores_imoveis (filial);

create index if not exists idx_acessos_bairro
	on curated.acessos_corretores_imoveis (bairro);

create index if not exists idx_acessos_tipo_imovel
	on curated.acessos_corretores_imoveis (tipo_imovel);

create index if not exists idx_acessos_funcao
	on curated.acessos_corretores_imoveis (funcao_acessada);

create index if not exists idx_acessos_codigo_imovel
	on curated.acessos_corretores_imoveis (codigo_imovel);

